    <script>
        // Generate pie chart based on credit score
        document.addEventListener('DOMContentLoaded', function() {
            const creditScore = <?php echo $creditReport['credit_score']; ?>;
            const total = 850; // Max possible score
            
            // Calculate percentages for visualization
            const poor = Math.min(399, creditScore) / total * 100;
            const fair = creditScore >= 400 ? Math.min(149, creditScore - 400) / total * 100 : 0;
            const good = creditScore >= 550 ? Math.min(99, creditScore - 550) / total * 100 : 0;
            const veryGood = creditScore >= 650 ? Math.min(99, creditScore - 650) / total * 100 : 0;
            const excellent = creditScore >= 750 ? (creditScore - 750) / total * 100 : 0;
            
            const svg = document.querySelector('.pie-chart');
            const centerX = 100;
            const centerY = 100;
            const radius = 80;
            
            let currentAngle = 0;
            
            function addSlice(angle, color) {
                if (angle <= 0) return;
                
                const startAngle = currentAngle * Math.PI / 180;
                const endAngle = (currentAngle + angle) * Math.PI / 180;
                
                const startX = centerX + radius * Math.cos(startAngle);
                const startY = centerY + radius * Math.sin(startAngle);
                const endX = centerX + radius * Math.cos(endAngle);
                const endY = centerY + radius * Math.sin(endAngle);
                
                const largeArcFlag = angle > 180 ? 1 : 0;
                
                const pathData = [
                    'M', centerX, centerY,
                    'L', startX, startY,
                    'A', radius, radius, 0, largeArcFlag, 1, endX, endY,
                    'Z'
                ].join(' ');
                
                const path = document.createElementNS('http://www.w3.org/2000/svg', 'path');
                path.setAttribute('d', pathData);
                path.setAttribute('fill', color);
                path.setAttribute('class', 'pie-slice');
                
                // Highlight the user's actual rating category
                if ((color === '#e74c3c' && creditScore < 400) ||
                    (color === '#f39c12' && creditScore >= 400 && creditScore < 550) ||
                    (color === '#3498db' && creditScore >= 550 && creditScore < 650) ||
                    (color === '#9b59b6' && creditScore >= 650 && creditScore < 750) ||
                    (color === '#2ecc71' && creditScore >= 750)) {
                    path.style.filter = 'drop-shadow(0 0 8px rgba(0,0,0,0.3))';
                    path.style.strokeWidth = '3';
                    path.style.stroke = '#fff';
                }
                
                svg.appendChild(path);
                currentAngle += angle;
            }
            
            // Add slices in order
            addSlice(poor, '#e74c3c');
            addSlice(fair, '#f39c12');
            addSlice(good, '#3498db');
            addSlice(veryGood, '#9b59b6');
            addSlice(excellent, '#2ecc71');
        });
    </script>
